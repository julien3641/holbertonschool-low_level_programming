#include "3-calc.h"

/**
 * op_add - additions
 * @a: a
 * @b: b
 * Return: Sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Difference
 * @a: a
 * @b: b
 * Return: Difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplication
 * @a: a
 * @b: b
 * Return: Multiplication
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Division
 * @a: a
 * @b: b
 * Return: Division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo
 * @a: a
 * @b: b
 * Return: Modulo
 */

int op_mod(int a, int b)
{
	return (a % b);
}
